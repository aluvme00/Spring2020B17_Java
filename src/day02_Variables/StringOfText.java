package day02_Variables;

public class StringOfText {

    public static void main(String[] args) {

        String employeeNam = "Sun Woo Kim";
        System.out.println(employeeNam);
        String companyNam = "Bank of America";
        System.out.println("My name is "+ employeeNam);
        System.out.println("The name of the company is "+companyNam);
        System.out.println(employeeNam+" works at "+companyNam);
        System.out.println();
        System.out.println("My name is "+ employeeNam +"\n" + "The name of the company is "+companyNam + "\n" + employeeNam+" works at "+ companyNam );

        System.out.println("      ^");
        System.out.println("     /|\\");
        System.out.println("    / | \\");
        System.out.println("   /  |  \\");
        System.out.println("   |  |  |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" |    |    |");
        System.out.println(" - - - - - -");

    }

}
