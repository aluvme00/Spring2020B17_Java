package day17_WhileLoops;

public class WhileLoop {
    public static void main(String[] args) {

        for (int i = 0; i <= 5; i++){
            System.out.println("Hello World");
        }
        int i = 0;
        while (i <= 5){
            i++;
            System.out.println(i);
        }
        while (true){
            System.out.println("Hello World");
        }
    }
}
